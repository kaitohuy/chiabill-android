import java.util.Properties
import java.net.URI

val localProperties = Properties()
val localPropertiesFile = rootProject.file("local.properties")
if (localPropertiesFile.exists()) {
    localPropertiesFile.reader().use { localProperties.load(it) }
}
val googleMapsApiKey = localProperties.getProperty("googleMapsApiKey") ?: ""

val envProperties = Properties()
val envFile = rootProject.file("../.env")
if (envFile.exists()) {
    envFile.reader().use { envProperties.load(it) }
}
val baseUrl = envProperties.getProperty("BASE_URL") ?: ""
val appLinkHost = try {
    URI(baseUrl).host ?: ""
} catch (e: Exception) {
    ""
}

val keystoreProperties = Properties()
val keystorePropertiesFile = rootProject.file("key.properties")
if (keystorePropertiesFile.exists()) {
    keystorePropertiesFile.reader().use { keystoreProperties.load(it) }
}

